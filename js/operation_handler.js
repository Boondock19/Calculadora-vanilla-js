


function operationHandler(firstNumber, secondNumber, operation){
  if (operation == "+") {
    console.log(`Esta es la operacion ${operation} entre
         ${firstNumber} y el segundo numero  ${secondNumber}`);
  } else if (operation == "-") {
    console.log(`Esta es la operacion ${operation} entre
         ${firstNumber} y el segundo numero  ${secondNumber}`);
  } else if (operation == "x") {
    console.log(`Esta es la operacion ${operation} entre
         ${firstNumber} y el segundo numero  ${secondNumber}`);
  } else if (operation == "/") {
    console.log(`Esta es la operacion ${operation} entre
         ${firstNumber} y el segundo numero  ${secondNumber}`);
  } else if (operation == "%") {
    console.log(`Esta es la operacion ${operation} entre
         ${firstNumber} y el segundo numero  ${secondNumber}`);
  } else if (operation == "mod") {
    console.log(`Esta es la operacion ${operation} entre
         ${firstNumber} y el segundo numero  ${secondNumber}`);
  }
};

export {operationHandler};
